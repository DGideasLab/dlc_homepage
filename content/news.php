									<h4 class="media-heading">发布CNTK认知工具集讲解</h4>
									<small><strong>北京，中国</strong> 2017年07月22日</small>
									<p>2017年07月22日，DGideas亚洲研究院在知乎开通了CNTK认知工具集的专栏。这个专栏将向用户讲解微软认知工具集（CNTK）的原理、方法以及应用：</p>
									<p>2016年1月25日，微软公司在他们的GitHub仓库上宣布开源了由微软研究院开发的计算网络工具集CNTK。早在2014年，在微软公司内部，黄学东博士和他的团队正在对计算机能够理解语音的能力进行更快的改进，但当时使用的工具显然拖慢了他们的进度。于是，一组由志愿者组成的开发团队构想设计了他们自己的解决方案，这项努力得到了回报。自从2016年1月微软公司宣布开源自家的认知工具集CNTK之后，在这一年多的时间里，经过开发者和开源社区的不断努力，已经取得了巨大的进步：从支持Python语言接口，到发布了工具集的2.0版本……CNTK工具正在在社区的推动下快速地发展着。CNTK 是一个统一的计算网络框架，它将深层神经网络描述为一系列通过有向图的计算步骤。在有向图中，每个节点代表一个输入值或一个网络参数，每个边表示在其中的一个矩阵运算。CNTK 提供了实现前向计算和梯度计算的算法。CNTK中预定义了很多主流的计算网络结构，用户可以轻松地在开源许可证下扩展节点类型。社区可以利用它来更方便地来推进关于人工智能的研究……</p>

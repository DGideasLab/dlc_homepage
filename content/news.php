									<h4 class="media-heading">为移动设备重新设计的响应式布局</h4>
									<small><strong>北京，中国</strong> 2017年07月28日</small>
									<p>最近几天，使用移动设备进行浏览的您可能会发现，我们对DGideas亚洲研究院的官方网站进行了重新设计，使其更适合于移动设备访问。这是我们为研究院官网上线一周年所做的改进计划之一：在我们浏览网页并且获得信息的过程中，人们更多地会去关注网页的具体内容，而非额外功能。秉承这个理念，我们的工程师删除了我们认为在移动设备上不需要出现的元素：比如下载栏目，额外信息和其他冗余装饰。现在看起来，DGideas亚洲研究院的官方网站在手机上浏览更加清爽了。同时，在平板电脑和小屏幕设备上的样式也会有不同：我们对于所有屏幕尺寸的设备的表现都进行了重新设计。</p>
									<p>事实上，在不改变网页原有结构并且对不同尺寸设备进行适配的过程，得益于<a href="//bootcss.com" target="_blank">BootStrap</a>的<strong>媒体查询</strong>功能。它能够使工程师在较短时间内，使用预定义的<strong>样式</strong>（Style），就能实现对不同屏幕尺寸设备的适应性匹配：比如，我们希望用户在手机等较小屏幕设备浏览时，不会出现桌面软件的下载链接。在传统网页的实现中，工程师通过JavaScript之类的脚本、或者PHP之类的后端语言查询用户的平台，从而针对不同的平台写出适应性的代码。而使用CSS的媒体查询，我们能够使用同一套代码来适配所有的设备。</p>
									<p>在未来的一段时间内我们仍将持续改进我们的官方网站，并添加一些有趣的功能，及时发布最新消息。</p>
